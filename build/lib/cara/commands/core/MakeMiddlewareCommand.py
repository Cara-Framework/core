"""
Middleware Generation Command for the Cara framework.
"""

from pathlib import Path

from cara.commands import CommandBase
from cara.decorators import command
from cara.support import paths


@command(
    name="make:middleware",
    help="Generate a new Middleware class with enhanced options.",
    options={
        "--dry": "Show what would be generated without creating files",
        "--force": "Overwrite existing middleware file",
    },
)
class MakeMiddlewareCommand(CommandBase):
    """Generate Middleware classes with enhanced configuration."""

    def handle(self, name: str):
        """Handle middleware generation."""
        self.info("ðŸ—ï¸  Middleware Generation")

        try:
            middleware_info = self._prepare_middleware_info(name)
        except ValueError as e:
            self.error(f"âŒ {e}")
            return

        if self._check_existing_file(middleware_info):
            return

        if self.option("dry"):
            self._show_dry_run(middleware_info)
            return

        try:
            self._generate_middleware(middleware_info)
        except Exception as e:
            self.error(f"âŒ Failed to generate middleware: {e}")

    def _prepare_middleware_info(self, name: str) -> dict:
        """Prepare middleware information."""
        if not name:
            raise ValueError("Middleware name is required")

        class_name = self._clean_class_name(name)
        middlewares_dir = Path(paths("middlewares"))
        file_path = middlewares_dir / f"{class_name}.py"

        return {
            "class_name": class_name,
            "file_path": file_path,
            "middlewares_dir": middlewares_dir,
        }

    def _clean_class_name(self, name: str) -> str:
        """Clean and format class name."""
        if name.endswith(".py"):
            name = name[:-3]
        name = "".join(word.capitalize() for word in name.replace("_", " ").split())
        if not name.endswith("Middleware"):
            name += "Middleware"
        return name

    def _check_existing_file(self, middleware_info: dict) -> bool:
        """Check if middleware file already exists."""
        if middleware_info["file_path"].exists() and not self.option("force"):
            self.warning(f"âš ï¸  Middleware already exists: {middleware_info['file_path']}")
            self.info("ðŸ’¡ Use --force to overwrite existing middleware")
            return True
        return False

    def _show_dry_run(self, middleware_info: dict) -> None:
        """Show dry run preview."""
        self.info("ðŸ” DRY RUN MODE - No files will be created")
        self.info("ðŸ“‹ Middleware configuration:")
        self.info(f"   Class Name: {middleware_info['class_name']}")
        self.info(f"   File Path: {middleware_info['file_path']}")

    def _generate_middleware(self, middleware_info: dict) -> None:
        """Generate the middleware file."""
        self.info("ðŸ”§ Configuration:")
        self.info(f"   Class: {middleware_info['class_name']}")
        self.info(f"   File: {middleware_info['file_path']}")

        middleware_info["middlewares_dir"].mkdir(parents=True, exist_ok=True)
        code = self._generate_middleware_code(middleware_info)

        self.info("âš¡ Generating middleware...")
        try:
            with open(middleware_info["file_path"], "w", encoding="utf-8") as f:
                f.write(code)

            self.info("âœ… Middleware created successfully!")
            self.info(f"ðŸ“ Location: {middleware_info['file_path']}")
            self._show_usage_tips(middleware_info)

        except Exception as e:
            raise Exception(f"Failed to write middleware file: {e}")

    def _generate_middleware_code(self, middleware_info: dict) -> str:
        """Generate the middleware class code."""
        stub_path = Path(paths("cara")) / "commands" / "stubs" / "Middleware.stub"

        with open(stub_path, "r", encoding="utf-8") as f:
            stub_content = f.read()

        code = stub_content.replace("{{ class_name }}", middleware_info["class_name"])
        code = code.replace(
            "{{ docstring }}",
            f"{middleware_info['class_name']}\n\nGenerated by Cara framework make:middleware command.",
        )

        return code

    def _show_usage_tips(self, middleware_info: dict) -> None:
        """Show usage examples."""
        class_name = middleware_info["class_name"]

        self.info("\nðŸ’¡ Usage Tips:")
        self.info(f"   Import: from app.middlewares import {class_name}")
        self.info("   Register middleware in Kernel.py:")
        self.info(f"     middleware = ['{class_name}']")
        self.info("   Apply to route:")
        self.info(f"     @Route(middleware=['{class_name.lower()}'])")
        self.info("   Apply to controller:")
        self.info(f"     @RouteGroup(middleware=['{class_name.lower()}'])")
